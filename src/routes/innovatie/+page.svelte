			<h1 class="mb-1 text-center text-2xl font-bold">Specifieke Modellen en Theorieën</h1>
			<p class="text-base/7 font-bold">Social Cognitive Theory (SCT) Bandura (2001)</p>
			<p class="mb-4 text-base/7">
				Deze theorie omschrijft dat kinderen leren door hun sociale omgeving te observeren en later
				deze handelingen of vaardigheden zelf te proberen om ze eigen te maken. Dit kan zeer nuttig
				worden ingezet om kinderen basisvaardigheden of gewoonten aan te leren. Echter kunnen er met
				de technologie van NOVA mogelijke complicaties ontstaan.
			</p>
			<p class="mb-4 text-base/7">
				Als een kind bijvoorbeeld nooit observeert hoe bepaalde basisvaardigheden of basisbehoeften
				worden uitgevoerd, zoals bijvoorbeeld koken of schoonmaken, dan zal een kind deze
				handelingen niet snel zelf uit kunnen voeren in het geval dat een technologie niet werkt.
				Dit heeft een sterke invloed op de autonomie en onafhankelijkheid van een kind. Het is
				daarom ethisch verantwoord dat verzorgers en de omgeving van een kind dat opgroeit met deze
				technologie, zich bijscholen over de mogelijkheden en gevaren van deze adaptatie en
				onafhankelijkheid.
			</p>
			<p class="mb-4 text-base/7">
				Echter beschrijft deze theorie ook dat kinderen technologie sneller adopteren dan ouderen.
				De adaptatie zal dus veel minder moeizaam gaan en eerder natuurlijk worden dan voor
				bijvoorbeeld een ouder persoon. De NOVA zal echter zeer gebruiksvriendelijk zijn en zich
				aanpassen aan de doelgroep, waardoor deze adaptatie waarschijnlijk vloeiend zal verlopen.
			</p>

			<p class="text-base/7 font-bold">Social Impact Theory (SIT) Jackson (1987)</p>
			<p class="mb-4 text-base/7">
				Deze theorie omschrijft de mate van acceptatie en adaptatie van nieuwe concepten door een
				omgeving. Deze zijn echter afhankelijk van een aantal factoren. Volgens de theorie hebben de
				Sterkte (de mate van aantrekkelijkheid/succes van een smart home), Directheid (de mate van
				nabijheid in de wijk) en het Aantal (het aantal smart homes in een wijk) invloed op het
				adopteren van deze nieuwe technologie of het concept van een smart home.
			</p>
			<p class="mb-4 text-base/7">
				Als er wordt gekeken naar de theorie in correlatie met de adaptatie van NOVA, kan deze
				technologie sneller geadapteerd worden. Dit hangt echter af van de bovengenoemde factoren.
				Ten eerste heeft de grootte van de groep die de NOVA gebruikt invloed op hoe snel een
				omgeving deze technologie in huis zal nemen. Als er in de directe omgeving van een persoon
				veel huishoudens zijn met de NOVA in huis, zal een individu sneller geneigd zijn deze
				technologie ook aan te schaffen.
			</p>
			<p class="mb-4 text-base/7">
				Daarnaast heeft de nabijheid een grote invloed op de adaptatie; de mate van nabijheid kan
				een grote motivatiefactor creëren. Als iemand in de directe omgeving zeer enthousiast is,
				kan deze motivatie worden opgewekt bij een individu. Als laatste is de sterkte een grote
				motivatiefactor. Als een invloedrijk persoon binnen een bepaalde bevolkingsgroep of sociale
				omgeving deze technologie sterk omarmt, zal dit een grote motivatie zijn om de NOVA te
				accepteren en te adopteren.
			</p>
