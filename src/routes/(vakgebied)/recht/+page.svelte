				empathie, ethisch inzicht en moreel oordeel essentieel zijn. AI kan bepaalde routinetaken
				sneller en efficiënter uitvoeren, maar het mist het vermogen om de nuances van menselijke
				emoties, spontaniteit van sociale relaties en de ethische dimensies van een zaak volledig te
				begrijpen en daar een oordeel over te kunnen geven.
