				<li>Virtuele rechtszalen worden wijdverbreid voor civiele geschillen.</li>
				<li>
					AI wordt geavanceerder en kan helpen bij complexe juridische besluitvorming en
					bemiddeling.
				</li>
			</ul>

			<p class="mt-4 text-base/7 font-bold">Lange termijn (2040-2045)</p>
			<ul class="ml-7 list-disc">
