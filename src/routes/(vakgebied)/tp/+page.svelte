				doelgroepen zoals kinderen en ouderen bij het leren omgaan met technologie. Op lange termijn
				verschuift de aandacht naar ethische kwesties en de maatschappelijke acceptatie van
				geavanceerde technologieën, waarbij het behoud van menselijke waarden centraal staat.
			</p>

			<p class="text-base/7">
