			<h1 class="mb-1 text-center text-2xl font-bold">Psychologische Effecten</h1>
			<p class="mb-4 text-base/7">
				In het heden zijn er al veel processen geautomatiseerd, zoals bijvoorbeeld de was doen, het
				huis verwarmen of de afwas doen. Deze automatisering zal zich alleen maar uitbreiden en
				toenemen, en kan zowel voor- als nadelen opleveren vanuit een psychologisch perspectief.
			</p>
			<p class="text-base/7 font-bold">Minder inspanning en autonomie</p>
			<p class="mb-4 text-base/7">
				Wanneer we kijken naar automatisering in en rondom een huis, kan dit ervoor zorgen dat de
				mens zich minder fysiek hoeft in te spannen. Denk hierbij aan het schoonmaken van het huis,
				wat geautomatiseerd kan worden. Dit creëert meer ruimte voor vrijetijdsbesteding (Fog,
				2003), maar kan ook leiden tot minder fysieke beweging. Het gevolg van minder fysieke
				activiteit kan zijn dat de mate van stressverwerking afneemt, wat mentale gevolgen kan
				hebben. Als een persoon minder fysieke activiteiten ervaart, kan dit leiden tot een
				verminderd energieniveau en vermoeidheid (Gonzalez, 2019).
			</p>

			<p class="mb-4 text-base/7">
				Daarnaast, als een persoon zijn dagelijkse taken minder of zelfs helemaal niet meer uit
				hoeft te voeren, kan dit leiden tot een gevoel van hulpeloosheid of afhankelijkheid. Deze
				gevoelens kunnen weer leiden tot mentale stress. Technologie en automatisering zijn zeer
				bruikbaar en nuttig in het dagelijks leven, maar wanneer deze technologie niet werkt, kunnen
				er gevolgen zijn als iemand hiervan afhankelijk is. Als de technologie bijvoorbeeld niet
				werkt en iemand is afhankelijk van deze technologie voor het huishouden of basisbehoeften,
				kan dit mentale stress opleveren. Dit is echter afhankelijk van de mate van afhankelijkheid
				(Lee, Y., 2018).
			</p>

			<h2 class="mb-1 text-left text-xl font-bold">Gevolgen voor kinderen en de opvoeding</h2>
			<p class="mb-4 text-base/7">
				Automatisering kan ook invloed hebben op de opvoeding van kinderen. Als een kind zijn ouders
				nooit een handeling of vaardigheid heeft zien uitvoeren, kan het moeilijker zijn voor het
				kind om deze vaardigheden zelf aan te leren. De Social Cognitive Theory (SCT) beschrijft dat
				kinderen leren door hun sociale omgeving te observeren en later deze handelingen of
				vaardigheden zelf proberen te leren (Bandura, 2001).
			</p>
			<p class="mb-4 text-base/7">
				Een voorbeeld hiervan is wanneer een kind nooit heeft gezien dat zijn ouders stofzuigen of
				dweilen, omdat dit altijd werd gedaan door een automatische stofzuiger/dweil technologie. In
				het geval dat de technologie niet werkt, zal een kind later niet zelf zijn huis kunnen
				onderhouden. De SCT benadrukt ook dat mensen vaardigheden moeten zien voordat ze deze kunnen
				leren en eigen maken. Daarom is het belangrijk om technologie te omarmen, maar ook te zorgen
				dat een persoon in staat blijft om deze vaardigheden zelf uit te voeren en niet volledig
				afhankelijk wordt van technologie.
			</p>
			<p class="text-base/7 font-bold">Sociale omgeving en status</p>
			<p class="mb-4 text-base/7">
				Binnen de gezinsdynamiek en sociale omgeving creëert automatisering zowel positieve als
				negatieve gevolgen. Door het creëren van meer vrije tijd kan er meer tijd worden besteed aan
				het gezin of een relatie binnen het huishouden. Daarnaast ontstaat er meer tijd voor sociale
				activiteiten of sport. Dit kan bijdragen aan de sociale cohesie in iemands leven en een
				betere band binnen het gezin.
			</p>
			<p class="mb-4 text-base/7">
				Echter, niet alle effecten van automatisering zijn positief. Het kan ook de samenwerking
				tussen gezinsleden of partners verminderen. Dit kan in uitzonderlijke gevallen leiden tot
				isolatie of een afname van gezinsactiviteiten (Srinivasan & McKeown, 2016).
			</p>
			<p class="mb-4 text-base/7">
				Hoe verder de technologie vordert, hoe groter de sociale druk wordt om deze ook te
				adopteren. Het bezit van geavanceerde technologie kan de perceptie van welvaart en status
				vergroten, wat positieve effecten kan hebben op de mentale gezondheid van een persoon.
				Echter, dit brengt ook sociale druk met zich mee om mee te draaien met de huidige adaptatie
				van automatiserende technologie. Deze sociale druk kan negatieve mentale gevolgen hebben,
				zoals stress (Harris, 2020). Waarschijnlijk zal niet iedereen in de toekomst deze
				technologie kunnen betalen, en dit kan een nog negatiever effect hebben op de mentale
				gesteldheid van mensen.
			</p>
			<p class="text-base/7 font-bold">Evaluatie</p>
			<p class="mb-4 text-base/7">
				Automatisering in het dagelijks leven biedt zowel voordelen als nadelen voor de mentale
				gezondheid. Enerzijds kan het leiden tot meer vrije tijd en verbeterde sociale interacties,
				maar anderzijds kan het ook gevoelens van hulpeloosheid, afhankelijkheid en sociale druk
				veroorzaken. Minder fysieke activiteit en een grotere afhankelijkheid van technologie kunnen
				mentale stress vergroten, vooral als technologie niet werkt. Daarnaast kan de druk om nieuwe
				technologie te bezitten leiden tot stress, vooral als dit niet voor iedereen financieel
				haalbaar is.
			</p>
			<p class="mb-4 text-base/7">
				De impact van automatisering op de autonomie en het welbevinden van een persoon hangt sterk
				af van de mate van afhankelijkheid en de sociale context. Dit heeft ook betrekking op het
				gebruik van de NOVA. Dezelfde frustratie kan ontstaan als de NOVA niet alle apparatuur en
				voorzieningen binnen het huis aanstuurt. Er kan een mogelijkheid ontstaan dat een persoon
				afhankelijk wordt van zowel de apparatuur als het smart home-systeem dat deze apparatuur
				aanstuurt. Hierdoor wordt de autonomie zo ver aangetast dat dit gevolgen kan hebben voor het
				welbevinden van deze persoon.
			</p>
			<p class="mb-4 text-base/7">
				Het is van belang dat alle belanghebbenden transparant zijn over de mogelijke psychologische
				gevolgen van automatisering, zoals de overheid, techbedrijven en consumenten. Als iedereen
				zich bewust is van de mogelijke gevolgen van automatisering, kan de kans op verlies van
				autonomie in de toekomst verminderd worden.
			</p>
		</div>
