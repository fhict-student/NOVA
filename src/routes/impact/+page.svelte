			<h1 class="mb-1 text-center text-2xl font-bold">Maatschappelijke Context</h1>
			<p class="mb-4 text-base/7">
				Een smart home heeft veel technologieën ingebouwd in het huis, waaronder veel
				geautomatiseerde apparaten die het huis onderhouden en een persoon van basisbehoeften
				voorzien. Daarnaast zijn camera’s een veelvoorkomende technologie die gebruikt worden in
				deze smart homes om de woning te beveiligen.
			</p>
			<p class="mb-4 text-base/7">
				De buren kunnen zich bekeken voelen door deze camera’s die worden gebruikt, waardoor de
				privacy aangetast kan worden. Van der Sloot (2017) beschrijft dat toezichtsystemen in
				woonwijken tot ergernis en frustratie kunnen zorgen. De privacy van mensen wordt aangetast,
				waardoor mensen zich niet meer op hun gemak voelen. Echter, kan dit ook voor een dermate
				gevoel van veiligheid zorgen, omdat de camera’s ook de buurt in de gaten kunnen houden.
				Hierin zit een ethische kwestie. Wij denken dat dit zo goed mogelijk opgelost kan worden
				door middel van buurtvergaderingen en het bespreken met de omwonenden. Het blijft echter wel
				een recht om dit uiteindelijk zelf te bepalen voor de eigen woning.
			</p>
			<p class="mb-4 text-base/7">
				Daarnaast kan de directe woonomgeving een mening hebben over de duurzaamheid van het
				stroomgebruik. Echter, hebben wij gekozen voor een redelijk zelfvoorzienend en groen huis.
				Dit kan kenbaar gemaakt worden aan de voorkant van de woning, zodat dit huis duurzaam
				gebruikt wordt, om mogelijke spanning in de woonomgeving te voorkomen. Uiteindelijk blijft
				het belangrijkste aspect van spanning in de woonomgeving communicatie. Als er goed
				gecommuniceerd wordt met omwonenden over mogelijke hindernissen van het smart home, kunnen
				deze zorgen mogelijk voorkomen worden.
			</p>
			<p class="mb-4 text-base/7">
				Smart homes maken veel gebruik van draadloze netwerken. Door de overconsumptie van deze
				zogeheten WSN-netwerken kunnen deze overbelast raken. Rashid (2016) beschrijft dat via
				landelijke en Europese wet- en regelgeving duidelijke afspraken en fondsen opgericht moeten
				worden om deze netwerken te versterken en te verbeteren. In de toekomst zullen deze
				WSN-netwerken alleen maar meer belast worden, en dus verbeterd moeten worden.
			</p>
			<p class="mb-4 text-base/7">
				Naast veiligheid en netwerk kan status ook problemen veroorzaken. Stern (2009) beschrijft
				dat het aantoonbaar hebben van nieuwe technologie geassocieerd wordt met welvaart. Dit kan
				jaloezie opwekken in de wijk. Naar onze schatting zal een smart home of een huis slim maken
				in 2045 meer betaalbaar zijn dan nu, maar zal dit nog steeds veel geld kosten.
			</p>
			<p class="mb-4 text-base/7">
				Wanneer het hebben van een smart home de norm wordt, is het mogelijk dat er een bepaalde
				groepsdruk ontstaat om zelf ook een smart home te kopen of te laten bouwen. Dit kan stress
				veroorzaken of een extra motivatie zijn voor de woonomgeving om zich toe te geven aan deze
				nieuwe technologie.
			</p>
			<p class="mb-4 text-base/7">
				De ‘Social Impact Theory’ (Jackson, 1987) beschrijft dat een omgeving geneigd kan zijn
				nieuwe concepten te adopteren. Dit hangt echter af van een aantal factoren. Volgens de
				theorie hebben de Sterkte (de mate van aantrekkelijkheid/succes van een smart home),
				Directheid (de mate van nabijheid in de wijk) en het Aantal (het aantal smart homes in een
				wijk) invloed op het adopteren van deze nieuwe technologie of het concept van een smart
				home.
			</p>
			<p class="text-base/7 italic">Evaluatie:</p>
			<p class="mb-4 text-base/7">
				Een smart hometechnologie in een wijk kan zowel voor- als nadelen hebben. Dit zal over 20
				jaar mogelijk nog niet de norm zijn, dus kunnen hier problemen ontstaan met omwonenden. Het
				bezit van nieuwe technologie binnen een smart home kan in een woonomgeving jaloezie of
				ergernis veroorzaken. Daarnaast kunnen omwonenden een onveilig gevoel ervaren door de breuk
				in privacy door de beveiligingscamera’s die zich in een smart home bevinden. De technologie
				die gebruikt wordt, kan het lokale netwerk overbelasten, wat meer ergernis of opstand kan
				veroorzaken bij omwonenden. Goede communicatie en afstemming in de buurt zijn van belang om
				een goede en veilige woonomgeving te creëren.
			</p>
