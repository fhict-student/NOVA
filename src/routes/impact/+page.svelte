			<h1 class="mb-1 text-center text-2xl font-bold">Maatschappelijke impact op gezinnen</h1>
			<p class="mb-4 text-base/7">
				In 2045 zal technologie een grote rol spelen in de manier waarop gezinnen functioneren in
				hun dagelijks leven. NOVA, een innovatieve smart home-assistent, heeft de potentie om het
				leven van een gezin te veranderen door in te spelen op de specifieke behoeften van de
				verschillende gezinsleden. In dit hoofdstuk wordt ingegaan op hoe NOVA in een gezin
				bestaande uit een 16-jarig kind, een 47-jarige moeder en een 73-jarige overgroot oma,
				functioneert binnen de diverse maatschappelijke contexten van onderwijs, werk, zorgtaken en
				gezondheidszorg. De technologie biedt niet alleen gemak, maar kan ook bijdragen aan het
				bevorderen van persoonlijke ontwikkeling, werkbalans, gezondheid en welzijn.
			</p>
			<h2 class="mb-1 text-left text-xl font-bold">Adoptie van technologie binnen het gezin</h2>
			<p class="mb-4 text-base/7">
				In relatie tot de adoptie van technologie kunnen verschillende belangrijke aspecten benoemd
				worden die het gebruik van NOVA door het gezin beïnvloeden. De adoptie van technologie is
				vaak afhankelijk van factoren zoals technologische bereidheid en de perceptie van het
				gebruiksgemak. Volgens het Technology Acceptance Model (TAM) van Davis (1989) speelt de
				waargenomen bruikbaarheid en de gebruiksvriendelijkheid van een technologie een cruciale rol
				in de beslissing om deze te adopteren. Dit geldt ook voor NOVA, waarbij het gezin
				waarschijnlijk het systeem zal omarmen als het intuïtief en gemakkelijk in gebruik is.
			</p>
			<p class="mb-4 text-base/7">
				Verder benadrukt de Diffusion of Innovations Theory van Rogers (2003) de invloed van sociale
				netwerken en culturele factoren bij de verspreiding van technologie. Het gezinslid dat het
				eerst met NOVA in contact komt, bijvoorbeeld de moeder of het kind, kan invloed hebben op de
				adoptie binnen het hele gezin, vooral als NOVA door hen als nuttig wordt ervaren in hun
				dagelijks leven. Tot slot kan de leeftijd van de gezinsleden ook invloed hebben. Onderzoek
				toont aan dat jongere mensen vaak sneller nieuwe technologieën omarmen vanwege hun digitale
				geletterdheid (Venkatesh & Bala, 2008), terwijl oudere generaties meer tijd nodig hebben om
				zich aan te passen en misschien meer begeleiding nodig hebben om de voordelen van NOVA in
				hun leven te zien.
			</p>
			<h3 class="mb-1 text-left text-lg font-bold">Onderwijs voor het Kind (16 jaar)</h3>
			<p class="mb-4 text-base/7">
				In 2045 zal het onderwijs voor jongeren steeds meer verweven zijn met technologie. NOVA
				speelt hierin een centrale rol door gepersonaliseerde studiebegeleiding te bieden die is
				afgestemd op de leerbehoeften van het kind. Dankzij de geïntegreerde kunstmatige
				intelligentie kan NOVA continu de leerprestaties van het kind monitoren en op basis van deze
				gegevens passende feedback en uitdagingen aanbieden. Dit stelt het kind in staat om op zijn
				of haar eigen tempo te leren, waarbij NOVA direct kan inspelen op zwakke punten en
				versterkende oefeningen kan aanbieden. In 2045 zal de meeste jeugd nog steeds naar school
				gaan, maar de manier waarop zij onderwijs ontvangen zal sterk beïnvloed worden door
				technologie zoals NOVA. NOVA zal fungeren als krachtige ondersteuning buiten de traditionele
				klasomgeving. Na schooltijd kan NOVA de leerstof verder verduidelijken, aanvullende
				oefeningen aanbieden of extra uitleg geven over onderwerpen die moeilijk waren. Zo kan het
				kind in zijn of haar eigen tempo leren. NOVA zal ook helpen bij het organiseren van
				huiswerk, het plannen van studietijd en het bieden van motivatie, wat het leerproces
				optimaliseert. Hoewel het kind naar school blijft gaan, fungeert NOVA als een persoonlijke
				studiebegeleider die het leren zowel op school als thuis ondersteunt, waardoor het
				leerproces gepersonaliseerd, dynamisch en effectief is.
			</p>
			<p class="mb-4 text-base/7">
				Een ander belangrijk aspect is de ondersteuning van digitale vaardigheden, essentieel voor
				de toekomst van werk en maatschappelijk functioneren. NOVA helpt niet alleen met academische
				taken, maar stimuleert ook het ontwikkelen van digitale geletterdheid door jongeren te
				begeleiden in het gebruik van technologie voor educatieve doeleinden. Dit helpt het kind om
				zich voor te bereiden op een digitale samenleving, waarin technologie een cruciale rol
				speelt (Binkley et al., 2012).
			</p>
			<p class="mb-4 text-base/7">
				Naast academische en digitale ondersteuning, speelt NOVA een belangrijke rol in het bewaken
				van de balans tussen schermtijd en mentale gezondheid. Door gebruik te maken van
				emotieherkenningstechnologie kan NOVA signaleren wanneer het kind zich gestrest of
				overweldigd voelt door te veel tijd achter schermen. Op basis van deze waarnemingen kan NOVA
				suggesties doen voor pauzes, fysieke activiteit of interactie met anderen, wat essentieel is
				voor het bevorderen van een gezonde mentale ontwikkeling (Rosen et al., 2013).
			</p>
