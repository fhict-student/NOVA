			<h1 class="mb-1 text-center text-2xl font-bold">
				Onafhankelijkheid en Gevolgen van Afhankelijkheid
			</h1>
			<p class="mb-4 text-base/7">
				Wanneer mensen technologie zo vaak gebruiken dat zij beweren niet meer zonder te kunnen, kan
				dit leiden tot afhankelijkheid. Bij de afhankelijkheid van technologie komen een aantal
				problemen kijken. Deze afhankelijkheid brengt verschillende problemen met zich mee. Naast de
				afhankelijkheid van de technologie zelf, kunnen mensen ook afhankelijk worden van de
				bedrijven die deze technologie en apparaten verkopen. Moeten deze bedrijven ethisch
				verantwoord handelen om een zo transparant mogelijke markt te creëren, met oog voor de
				veiligheid en de effecten van de afhankelijkheid die door hun producten wordt veroorzaakt
				(Blind, et al., 2021)?
			</p>
			<p class="mb-4 text-base/7">
				Het gemak en de afhankelijkheid van technologie hebben invloed op de cognitieve vermogens
				van mensen (Anderson & Rainie, 2020). Een voorbeeld hiervan is het gebruik van zoekmachines
				en smart assistentie, die het probleemoplossend vermogen en diep denken kunnen verminderen.
				Daarnaast kan de verslaving aan sociale media en de eindeloze stroom van informatie de
				aandachtspanne van een persoon verkorten, wat negatieve gevolgen kan hebben voor het
				cognitieve functioneren.
			</p>
			<p class="mb-4 text-base/7">
				Als er gekeken wordt naar een smart home, kunnen mensen incompetent worden in het uitvoeren
				van simpele taken. Naast fysieke afhankelijkheid komt er ook een afhankelijkheid van
				bedrijven bij kijken. Als één bedrijf bijvoorbeeld het monopolie op de smart home-software
				heeft, kan dit bedrijf ridicule prijzen vragen voor deze software of de gegevens verkopen.
				Om deze reden kunnen mensen zodanig afhankelijk worden van deze bedrijven om hun dagelijks
				leven en behoeften te vervullen. Dit is een ethische kwestie. Moet dit bij de bedrijven
				liggen of bij de overheid en wetgeving?
			</p>
			<p class="text-base/7 italic">Wat kun je doen?</p>
			<p class="mb-4 text-base/7">
				Om de afhankelijkheid van technologie en bedrijven te verminderen kunnen er een aantal
				interventies ondernomen worden. Er kan hierbij gedacht worden aan het gebruiken van eigen
				servers; hierbij maak je zelf een server zodat een bedrijf geen volledige controle heeft
				over je gegevens (Stallman, 2015).
			</p>
			<p class="mb-4 text-base/7">
				Naast eigen servers is het voordeliger voor de consument om gebruik te maken van zogeheten
				‘open source software’ (OSS). Hierdoor kan de software gecontroleerd en aangepast worden, en
				is een consument niet afhankelijk van de software die een bedrijf aanbiedt. Dit kan
				bijdragen aan meer onafhankelijkheid en veiligheid (Lane, 2011).
			</p>
			<p class="mb-4 text-base/7">
				Mensen bijscholen over het verzamelen van gegevens en de afhankelijkheid kan een grote
				bijdrage leveren aan de bewustwording. Met deze bewustwording kunnen mensen bewuster omgaan
				met deze technologie, met als gevolg bijvoorbeeld het gebruik van deze technologie
				verminderen om nog wel zelfstandig te kunnen leven (Anderson & Rainie, 2020).
			</p>
			<p class="mb-4 text-base/7">
				Een andere factor die kan spelen bij het meer onafhankelijk zijn, is het bijscholen van
				mensen over het onderhoud en de werking van de apparaten en software. Hierdoor hoeft een
				consument niet volledig afhankelijk te zijn van externe partijen en kunnen deze consumenten
				eerder zelf problemen oplossen als deze optreden (Zuboff, 2019).
			</p>
			<p class="mb-4 text-base/7">
				Doordat technologie alle dagelijkse handelingen overneemt, kan een persoon meer tijd
				besteden aan zijn sociale interacties. Echter, kan deze afhankelijkheid ook een negatief
				effect hebben: namelijk het onvermogen om basishandelingen uit te voeren als deze
				technologie niet werkt zoals gewoonlijk. Uiteindelijk zal er een goed evenwicht moeten
				ontstaan tussen gemak door middel van technologie en de eigen autonomie over het gebruik en
				beheer hiervan.
			</p>
			<p class="text-base/7 italic">Evaluatie</p>
			<p class="mb-4 text-base/7">
				Het gebruik van de bovenstaande technologie kan een persoon helpen in zijn dagelijkse leven.
				Echter, ontstaat hier een ethisch dilemma. Worden mensen te lui en incompetent om
				basisvaardigheden uit te voeren, of biedt het gemak en comfort in het leven, zodat we meer
				tijd kunnen investeren in sociale contacten en vrijetijdsbestedingen?
			</p>
			<p class="mb-4 text-base/7">
				Wij denken dat wanneer de verantwoordelijkheid wordt gelegd bij de bedrijven en de overheid
				om gebruikers bewust te maken van de gevolgen van deze automatisering en wat dit kan doen
				met een persoon en leefomgeving. Daarnaast vinden wij het belangrijk dat de consumenten
				worden ingelicht over de mogelijkheden van de producten, maar daarnaast ook over de gevaren
				rondom veiligheid en psychologische effecten.
			</p>
			<p class="mb-4 text-base/7">
				Daarnaast vinden wij dat grote techbedrijven de verantwoordelijkheid moeten dragen om
				onafhankelijkheid van deze bedrijven te waarborgen. Hierdoor kunnen consumenten van deze
				technologie zelf bepalen hoe ze de producten inzetten en hun privacy waarborgen. Daarnaast
				is de kans op een techmonopolie kleiner, omdat de consument zelf kan bepalen waar hij of zij
				de producten voor hun smart home koopt, in plaats van dat de bedrijven bepalen wat je moet
				kopen zodat de producten functioneren zoals ze bedoeld zijn.
			</p>
		</div>
